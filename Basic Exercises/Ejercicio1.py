#crear un programa que, al recibir como datos doa numeros enteros, calcule la suma, resta multiplicacion de dicho numero.

numero1 = float(input("Ingrese su primer número: "))
numero2 = float(input("Ingrese su segundo número: "))

print(f"La suma de {numero1} y {numero2} es: {numero1 + numero2}")
print(f"La resta de {numero1} y {numero2} es: {numero1 - numero2}")


